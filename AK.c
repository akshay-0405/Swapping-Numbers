#include<stdio.h>
void main()
{
	//Took two variables of datatype integer.
	int a,b;
	//Accepting 1st integer from user.
	printf("Enter 1st integer:");
	scanf("%d",&a);
	//Accepting 2nd integer from user.
	printf("Enter 2nd Integer:");
	scanf("%d",&b);
	//Swapping Them. 
	printf("After Swapping,1st integer=%d",b);
	printf("\nAfter Swapping,2nd Integer=%d",a);
}  
